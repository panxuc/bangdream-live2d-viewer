  libarchiveScript: "/libarchive.js",
  libarchiveWasm: "/libarchive.wasm",
  sevenZipScript: "/7zz.umd.js",
  sevenZipWasm: "/7zz.wasm",
  live2dScripts: ["/live2d.min.js", "/live2dcubismcore.min.js"],
